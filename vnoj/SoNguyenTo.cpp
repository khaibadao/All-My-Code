using namespace std;

typedef unordered_map<long long, long long> unmap;

vector <bool> prime(mang + 5);
void sang(){
	for(int i = 0 ; i <= 5*1000000 ; ++i){
		prime[i] = true;
	}
	prime[0] = prime[1] = 0;
	for(int i = 2 ; i <= sqrt(5*1000000) ; ++i){
		if(prime[i]){
			for(int j = i*i ; j <= 5*1000000 ; j += i){
				prime[j] = false;
			}
		}
	}
}

int main() {
    //freopen("", "r", stdin); 
    //freopen("", "w", stdout);
    ios_base::sync_with_stdio(false);
  	cin.tie(NULL); cout.tie(NULL);
	string s; cin >> s;
	sang();
	long ans = 0;
	for(int i = 0 ; i < Siz(s) ; ++i){
		if('0' <= s[i] && s[i] <= '9'){
			long so = 0;
			for(int j = i ; j < i + 7; ++j){
				if('0' <= s[j] && s[j] <= '9'){
					so *= 10;
					so += (s[j] - '0');
					if(so >= mang) break;
					if(prime[so]) ans = max(ans, so);
				}else break;
			}
		}
	}
	cout << ans;
	return 0;
}

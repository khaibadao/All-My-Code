using namespace std;

typedef unordered_map<long long, long long> unmap;
bool check = false;

long long ans = INT_MIN;
void doo(int n, long long k, vector <long long> &a){
	for(int i = 0 ; i < Siz(a) ; ++i){
		long long sum_tmp = 0;
		long long sum_tmp_chia = 0;
		for(int j = i ; j < Siz(a) ; ++j){
			sum_tmp += a[j];
			sum_tmp_chia = sum_tmp;
			sum_tmp_chia /= (j - i + 1);
			if(sum_tmp >= k){
				ans = max(ans, sum_tmp_chia);
				check = true;
			}
		}
	}
}

int main() {
    //freopen("", "r", stdin); 
    //freopen("", "w", stdout);
    ios_base::sync_with_stdio(false);
  	cin.tie(NULL); cout.tie(NULL);
	int n; cin >> n;
	long long k; cin >> k;
	vector <long long> a;
	for(int i = 0; i < n ; ++i){
		long long x; cin >> x;
		a.pb(x);
	}
	doo(n, k, a);
	(check == true) ? cout << ans : cout << "NO";
	return 0;
}

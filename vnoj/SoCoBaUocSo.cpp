using namespace std;

typedef unordered_map<long long, long long> unmap;

unmap maa;
vector <bool> prime(mang);
void sang(){
	for(int i = 0 ; i <= 1000000 ; ++i){
		prime[i] = true;
	}
	prime[0] = prime[1] = 0;
	for(int i = 2 ; i <= sqrt(1000000) ; ++i){
		if(prime[i]){
			for(int j = i*i ; j <= 1000000 ; j += i){
				prime[j] = false;
			}
		}
	}
}

int main() {
    //freopen("", "r", stdin); 
    //freopen("", "w", stdout);
    ios_base::sync_with_stdio(false);
  	cin.tie(NULL); cout.tie(NULL);
  	sang();
	int n; cin >> n;
	vector <long long> a(n);
	for(int i = 0 ; i < n ; ++i) cin >> a[i];
	for(long long x : a){
		long x1 = sqrt(x);
		(prime[x1] && x1*x1 == x) ? cout << "YES" << en : cout << "NO" << en;
	}
	return 0;
}

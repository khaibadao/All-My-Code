using namespace std;

typedef unordered_map<long long, long long> unmap;

long long a, b;
long do_nen = 0;
void doo(long long n){
	int res = 0;
	while(n != 2){
		for(long long i = 2 ; i < n ; ++i){
			if(n%i != 0){
				n = i;
				++res;
				break;
			}
		}
	}
	do_nen += res;
}

void xu_ly(){
	long long n = a;
	while(a <= n && n <= b){
		long tmp = n;
		doo(n);
		// cout << do_nen << ' ';
		n = tmp;
		++n;
	}
}


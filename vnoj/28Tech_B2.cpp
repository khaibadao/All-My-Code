void hinh1(int n){
	for(int i = 1 ; i <= n ; ++i){
		for(int j = 1 ; j <= i ; ++j){
			cout << '*';
		}
		cout << en;
	}
}

void hinh2(int n){
	for(int i = n ; i >= 1 ; i--){
		for(int j = 1 ; j <= i ; ++j){
			cout << '*';
		}
		cout << en;
	}
}

void hinh3(int n){
	string s = "*";
	int tmp = 4;
	for(int i = 1 ; i <= n ; ++i){
		for(int j = 1 ; j <= tmp; ++j){
			cout << ' ';
		}
		--tmp;
		cout << s;
		s = s + '*';
		cout << en;
	}
}

void hinh4(int n){
	string s = "";
	for(int i = 1 ; i <= n ; ++i) s += '*';
	int tmp = 0;
	for(int i = 1 ; i <= n ; ++i){
		for(int j = 1 ; j <= tmp ; ++j){
			cout << ' ';
		}
		cout << s;
		s.pop_back();
		++tmp;
		cout << en;
	}
}

void hinh5(int n){
	for(int i = 1 ; i <= n ; ++i){
		for(int j = 1 ; j <= i ; ++j){
			if(i == 1 || i == n){
				cout << '*';
			}
			else
			{
				if(j == 1 || j == i) cout << '*';
				else cout << ' ';
			}
		}
		cout << en;
	}
}

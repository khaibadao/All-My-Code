using namespace std;

typedef unordered_map<long long, long long> unmap;

vector <long> uoc;

bool check(long so_giua, long soluong, long long m){ // kiem tra co hop le khong
	long long ve_trai = so_giua - 2, ve_phai = so_giua + 2;
	long l = 0, r = 0;
	long long sum = so_giua;
	// cout << "so giua : " << so_giua << en;
	while(ve_trai > 1 && sum < m){
		if(sum == m) break;
		sum += ve_trai;
		sum += ve_phai;
		ve_trai -= 2;
		ve_phai += 2;
		// cout << ve_trai << "(vt)" << ' ' << ve_phai << "(vp)" << en; 
		++l, ++r;
	}
	return l + r == soluong - 1;
}

void sang(long long so)
{
	long so_giua = 0;
	long soluong = 0;
	// số lượng số chính là i
	for(long i = 1 ; i <= sqrt(so); i += 2){
		if(so%i == 0){
			if(check(so/i, i, so)){
				if(i > soluong){
					so_giua = so/i;
					soluong = i;
				}
			}
		}
	}
	cout << so_giua;
}

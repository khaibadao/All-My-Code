void doo(vector <int> &ans, int n){
	int sum = 0;
	for(int i = 1 ; i <= sqrt(n) ; ++i){
		if(n%i == 0){
			(i != n/i) ? sum += (n/i + i) : sum += i;
		}
	}
	if(sum >= 2*n) ans.pb(n);
}

using namespace std;

typedef unordered_map<long long, long long> unmap;
vector <long long> a;

void sang(long long D){
	long long res = 1;
	while(res*res <= D){
		a.pb(res*res);
		++res;
	}
	// a.pb(res*res);
}

long long ans = INT_MAX;
void doo(long long D) {
    sang(D);
    // for(long long x : a) cout << x << ' ';
    // cout << en; 
    for (int i = Siz(a) - 1; i >= 1; i--) {
		for(int j = i - 1 ; j >= 0 ; j--){
			long long sum_tmp = abs(a[i] + a[j] - D);
			ans = sum_tmp;
		}
    }
    cout << ans;
}



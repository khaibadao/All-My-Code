unmap maa;
bool check(int n){
	string x = to_string(n);
	for(char t : x){
		if(t > '1') return false;
	}
	return true;
}
void ql(int pos, int res, int so){
	if(res == c){
		if(!maa[so]){
			ans.pb(so);
			++maa[so];
		}
		return;
	}
	for(int i = 0 ; i <= 1 ; ++i){
		so *= 10;
		so += i;
		ql(i, res + 1, so);
		so -= i;
		so /= 10;
	}
}

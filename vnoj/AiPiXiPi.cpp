using namespace std;

typedef unordered_map<long long, long long> unmap;

bool kt(int a, int b, int c){
	if(a >= 1 && b >= 1 && c >= 1) return false;
	return true;
}

void doo() {
    int n;
    cin >> n;
    vector<long> a(n), b;
    unmap maa_a, maa_b, maa_c;
    
    // Đọc và lưu tần suất của mảng a
    for (long &x : a) {
        cin >> x;
        ++maa_a[x];
        if (maa_a[x] == 1) b.pb(x); // Chỉ thêm phần tử vào b nếu nó chưa xuất hiện trước đó
    }
    
    for (long &x : b) ++maa_b[x];

    long long ans = 0;
    
    // Tìm các bộ ba (i, j, k) thỏa mãn
    for (int i = 0; i < Siz(b) - 1; ++i) {
        for (int j = i + 1; j < Siz(b); ++j) {
            long long sum = b[i] + b[j];
            --maa_a[b[i]];
            --maa_a[b[j]];
            if (maa_a[-1*sum] > 0 && kt(maa_c[b[i]], maa_c[b[j]], maa_c[-1*sum])) {
                ++ans;
                ++maa_c[b[i]];
                ++maa_c[b[j]];
                ++maa_c[-1*sum];
                // cout << b[i] << ' ' << b[j] << ' ' << sum*-1 << en;
            }
            ++maa_a[b[i]];
            ++maa_a[b[j]];
        }
    }
    cout << ans << en;
}

int main() {
    //freopen("", "r", stdin); 
    //freopen("", "w", stdout);
    ios_base::sync_with_stdio(false);
  	cin.tie(NULL); cout.tie(NULL);
	int t; cin >> t;
	while(t--){
		doo();
	}
	// cout << 1;
	return 0;
}

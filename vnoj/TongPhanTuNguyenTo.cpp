using namespace std;

typedef unordered_map<long long, long long> unmap;

bool check(int n){
	if(n < 2) return false;
	if(n < 4) return true;
	if(n%2 == 0 || n%3 == 0) return false;
	for(int i = 5 ; i*i <= n ; i += 6){
		if(n%i == 0 || n%(i + 2) == 0) return false;
	}
	return true;
}

int main() {
    //freopen("", "r", stdin); 
    //freopen("", "w", stdout);
    ios_base::sync_with_stdio(false);
  	cin.tie(NULL); cout.tie(NULL);
	int n; cin >> n;
	vector <int> a(n);
	for(int &x : a) cin >> x;
	int ans = 0;
	for(int i = 0 ; i < n ; ++i){
		long sum = 0;
		for(int j = i ; j < n ; ++j){
			sum += a[j];
			if(check(sum)) ++ans;
		}
	}
	cout << ans;
	return 0;
}

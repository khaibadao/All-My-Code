using namespace std;

typedef unordered_map<long long, long long> unmap;

int kt_so(string s){
	int res = 0;
	for(char x : s){
		if('0' <= x && x <= '9') ++res;
	}
	return res;
}
int main() {
    freopen("TROCHOI.INP", "r", stdin); 
    freopen("TROCHOI.OUT", "w", stdout);
    ios_base::sync_with_stdio(false);
  	cin.tie(NULL); cout.tie(NULL);
  	int t; cin >> t;
  	vector <string> xa[256];
  	vector <int> si;
  	unmap maa;
  	while(t--){
  		string s; cin >> s;
  		int Si = kt_so(s);
  		xa[Si].pb(s);
  		si.pb(Si);
  		++maa[Si];
  	}
  	sort(si.begin(), si.end());
  	for(int i = 0 ; i < Siz(si) ; ++i){
  		if(maa[si[i]]){
  			for(const auto &it : xa[si[i]]){
  				cout << it << en;
  			}
  			maa[si[i]] = 0;
  		}
  	}
	return 0;
}

using namespace std;

typedef unordered_map<long long, long long> unmap;

struct pair_hash {
    template <typename T1, typename T2>
    size_t operator()(const pair<T1, T2>& p) const {
        auto hash1 = hash<T1>{}(p.first);  // Băm phần tử đầu tiên
        auto hash2 = hash<T2>{}(p.second); // Băm phần tử thứ hai
        return hash1 ^ (hash2 << 1);       // Kết hợp hai giá trị băm
    }
};

int main() {
    //freopen("", "r", stdin); 
    //freopen("", "w", sdout);
    ios_base::sync_with_stdio(false);
  	cin.tie(NULL); cout.tie(NULL);
	int n; cin >> n;
	vector <long> a, b;
	unordered_map<pair<long, long>, long, pair_hash> paa;
	unmap maaA, maaB;
	int lap = 0;
	while(n--){
		long x, y; cin >> x >> y;
		pair <long, long> k {x, y};
		++maaA[x], ++maaB[y];
		++paa[k];
		if(paa[k] >= 2) ++lap;
		a.pb(x), b.pb(y);
	}
	long long cap = 0;
	for(int i = 0 ; i < Siz(a) ; ++i){
		if(maaA[a[i]] >= 2){
			cap += maaA[a[i]] - 1;
			--maaA[a[i]];
		}
	}
	for(int i = 0 ; i < Siz(b) ; ++i){
		if(maaB[b[i]] >= 2){
			cap += maaB[b[i]] - 1;
			--maaB[b[i]];
		}
	}
	cout << cap - lap;
	return 0;
}

typedef unordered_map<long long, long long> unmap;

int main() {
    //freopen("", "r", stdin); 
    //freopen("", "w", stdout);
    ios_base::sync_with_stdio(false);
  	cin.tie(NULL); cout.tie(NULL);
	int ans = 1000000;
	int n; cin >> n;
	vector <int> a(n);
	int sum = 0;
	for(int &i : a){
		cin >> i;
		sum += i;
	}
	sort(a.begin(), a.end());
	for(int i = 0; i < n ; ++i){
		int sum1 = 0;
		for(int j = i ; j < n - 1 ; ++j){
			sum1 += a[j];
			int sum2 = sum - sum1;
			// cout << sum1 << ' ' << sum2 << ' ' << ans << en;
			if(sum2 - sum1 >= 0) ans = min(ans, sum2 - sum1);
		}
	}
	cout << ans;
	return 0;
}

void tach(string s){
  string so = "";
  for(int i = 0 ; i < Siz(s) ; ++i){
    if('0' <= s[i] && s[i] <= '9') so += s[i];
    else
    {
      if(Siz(so)){
        long long ans = stoll(so);
        a.pb(ans);
        so = "";
      }
    }
  }
  if(Siz(so)){
    long long ans = stoll(so);
    a.pb(ans);
    so = "";
  }
}

bool check_cp(long long n){
  long long x = sqrt(n);
  return x*x == n;
}

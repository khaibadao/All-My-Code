typedef unordered_map<long long, long long> unmap;

set <long long> st;
void uoc(long long n){
  for(long i = 1 ; i <= sqrt(n) ; ++i){
    if(n%i == 0){
      if(i != n/i){
        st.insert(i);
        st.insert(n/i);
      }else st.insert(i);
    }
  }
}


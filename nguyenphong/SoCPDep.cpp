using namespace std;

typedef unordered_map<long long, long long> unmap;

vector <long long> ans;
bool check(long long n){
	string s = to_string(n);
	int l = 0, r = Siz(s) - 1;
	while(l < r){
		if(s[l] == s[r]){
			++l, --r;
		}else return false;
	}
	return true;
}
vector <int> prime(10000001);
void sang()
{
	for(long i = 1 ; i <= 10000000 ; ++i) prime[i] = 1;
	prime[0] = prime[1] = 0;
	for(int i = 2 ; i <= sqrt(10000000) ; ++i){
		if(prime[i]){
			for(long j = i*i ; j <= 10000000 ; j += i){
				prime[j] = 0;
			}
		}
	}
	for(int i = 2 ; i <= 10000000 ; ++i){
		if(prime[i]){
			if(check(1ll*i*i)){
				ans.pb(1ll*i*i);
			}
		}
	}
}


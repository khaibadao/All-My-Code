using namespace std;

typedef unordered_map<long long, long long> unmap;

char H(int n, char a)
{
	char kt = a;
	for(int i = 0 ; i < n ; ++i){
		if(kt == 'Z') kt = 'A';
		else ++kt;
	}
	return kt;
}


char T(int n, char a)
{
	char kt = a;
	for(int i = 0 ; i < n ; ++i){
		if(kt == 'z') kt = 'a';
		else ++kt;
	}
	return kt;
}


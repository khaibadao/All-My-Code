using namespace std;

unordered_map <int, int> maa;

void chay(int a, int b, int &ans){
	int res = 0;
	for(int i = a ; i <= b ; ++i){
		if(!maa[i]){
			++maa[i];
			++res;
		}
	}
	ans = res;
}


int main() {
    ios_base::sync_with_stdio(false);
	cin.tie(NULL); cout.tie(NULL);
	int n, S; cin >> n >> S;
	int toi_da = 0, toi_thieu = 0;
	while(n--){
		int a, b; cin >> a >> b;
		toi_da = max(toi_da, b - a + 1);
		int tmp = 0;
		chay(a, b, tmp);
		toi_thieu = max(toi_thieu, tmp);
	}
	cout << toi_da << ' ' << toi_thieu;
	return 0;
}

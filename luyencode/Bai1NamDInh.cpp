using namespace std;

typedef unordered_map<long long, long long> unmap;

unmap maa;
vector <long> a(mang, 0);

bool check(long a, long b){
	return 2*a <= b;
}

void sang()
{
	for(int i = 1 ; i <= 1000 ; ++i){
		for(int j = i ; j <= 1000000 ; j += i){
			a[j] += i;	
			++maa[a[j]];
			// cout << a[j] << ' ' << j << en;
		}
		// cout << en;
	}
	// for(int i = 1 ; i <= 1000000 ; ++i) cout << a[i] << ' ';
	// cout << en;
}

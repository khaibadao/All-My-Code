using namespace std;

int n, q;

int a[mang];
long t[mang*4];

void build(int l, int r, int id){
  if(l == r){
    t[id] = a[l];
    return;
  }
  int mid = (l + r)/2;
  build(l, mid, id*2);
  build(mid + 1, r, id*2+1);
  t[id] = t[id*2] + t[id*2+1];
}

long get(int l, int r, int u, int v, int id){
  if(u > r || v < l) return 0;
  if(u <= l && r <= v) return t[id];

  int mid = (l + r)/2;
  return get(l, mid, u, v, id*2) + get(mid + 1, r, u, v, id*2+1);
}

void update(int l, int r, int chi_so, int gia_tri, int id){
  if(chi_so < l || chi_so > r) return;
  if(l == r){
    t[id] += gia_tri;
    return;
  }
  int mid = (l + r)/2;
  update(l, mid, chi_so, gia_tri, id*2);
  update(mid + 1, r, chi_so, gia_tri, id*2+1);
  t[id] = t[id*2] + t[id*2+1];
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);
    
    cin >> n >> q;

    for(int i = 1 ; i <= n ; ++i) cin >> a[i];
    build(1, n, 1);
    while(q--){
      int type; cin >> type;
      if(type == 1){
        int chi_so, gia_tri; cin >> chi_so >> gia_tri;
        update(1, n, chi_so, gia_tri, 1);
      }
      else
      {
        int u, v; cin >> u >> v;
        long res = get(1, n, u, v, 1);
        cout << res << en;  
      }
    }
    return 0;
}
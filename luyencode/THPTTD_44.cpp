using namespace std;

long cont(int l, int r, int tmp, vector <int> a){
	int res = 0;
	for(int i = l ; i <= r ; ++i){
		if(a[i]%tmp == 0 || tmp%a[i] == 0) ++res;
	}
    return res;
}

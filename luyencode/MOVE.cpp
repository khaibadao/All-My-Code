using namespace std;

int n, m;
int a[501][501];
int vst[503][503];

int main() {
    ios_base::sync_with_stdio(false);
	cin.tie(NULL); cout.tie(NULL);

	cin >> n >> m;
	for(int i = 1;  i <= n ; ++i){
		for(int j = 1 ; j <= m ; ++j){
			cin >> a[i][j];
		}
	}
	
	memset(vst, 0, sizeof(vst));
	for(int i = 1 ; i <= n ; ++i){
		for(int j = 1 ; j <= m ; ++j){
			if(i == 1) vst[i][j] = vst[i][j - 1] + a[i][j];
			else if(j == 1) vst[i][j] = vst[i - 1][j] + a[i][j];
			else vst[i][j] = max(vst[i - 1][j], vst[i][j - 1]) + a[i][j];
		}
	}
	cout << vst[n][m];
	return 0;
}
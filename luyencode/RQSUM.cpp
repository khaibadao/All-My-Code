using namespace std;

int n, q;
long long t[mang*4], a[mang], lazy[mang*4];

void doo(int id, int l, int r){
	if(lazy[id]){
		int mid = (l + r)/2;
		t[id*2] += lazy[id] * (mid - l + 1);
		t[id*2+1] += lazy[id] * (r - mid);
		lazy[id*2+1] += lazy[id];
		lazy[id*2] += lazy[id];
		lazy[id] = 0;
	}
}

void build(int l, int r, int id){
	if(l == r){
		t[id] = a[l];
		return;
	}
	int mid = (l + r)/2;
	build(l, mid, id*2);
	build(mid + 1, r, id*2+1);
	t[id] = t[id*2] + t[id*2+1];
}

long long get(int l, int r, int u, int v, int id){
	if(u > r || v < l) return 0;
	if(u <= l && r <= v) return t[id];
	int mid = (l + r)/2;
	doo(id, l, r);
	return get(l, mid, u, v, id*2) + get(mid + 1, r, u, v, id*2+1);
}

void update(int l, int r, int u, int v, long gia_tri, int id){
	if(u > r || v < l) return;
	if(u <= l && r <= v){
		t[id] += gia_tri * (r - l + 1);
		lazy[id] += gia_tri;
		return;
	}
	doo(id, l, r);
	int mid = (l + r)/2;
	update(l, mid, u, v, gia_tri, id*2);
	update(mid + 1, r, u, v, gia_tri, id*2+1);
	t[id] = t[id*2] + t[id*2+1];
}

int main() {

    ios_base::sync_with_stdio(false);
	cin.tie(NULL); cout.tie(NULL);

	cin >> n >> q;
	memset(lazy, 0, sizeof(lazy));

	for(int i = 1 ; i <= n ; ++i) cin >> a[i];
	build(1, n, 1);

	while(q--){
		int type; cin >> type;
		if(type == 1){
			int u, v;
			long gia_tri;
			cin >> u >> v >> gia_tri;
			update(1, n, u, v, gia_tri, 1);
		}
		else
		{
			int u, v; cin >> u >> v;
			long long res = get(1, n, u, v, 1);
			cout << res << en;
		}
	}

	return 0;
}

using namespace std;

bool ngto(long n){
	if(n < 2) return false;
	if(n < 4) return true;
	if(n%2 == 0 || n%3 == 0) return false;
	for(long i = 5; i*i <= n ; i += 6){
		if(n%i == 0 || n%(i + 2) == 0) return false;
	}
	return true;
}

bool check(long n){
	string s = to_string(n);
	long sum = 0;
	for(char x : s){
		sum += (x - '0')*(x - '0');
	}
	return ngto(sum);
}

using namespace std;


int n, q; 
long a[mang], t[mang*4];

void build(int l, int r, int id){
	if(l == r){
		t[id] = a[l];
		return;
	}

	int mid = (l + r)/2;

	build(l, mid, id*2);
	build(mid + 1, r, id*2+1);
	t[id] = max(t[id*2], t[id*2+1]);	
}

long get(int l, int r, int u, int v, int id){
	if(u > r || v < l) return INT_MIN;
	if(u <= l && r <= v) return t[id];

	int mid = (l + r)/2;
	return max(get(l, mid, u, v, id*2), get(mid + 1, r, u, v, id*2+1));
}


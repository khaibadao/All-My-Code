using namespace std;

int n, q;
long long a[mang], t[mang*4], lazy[mang*4];

void doo(int id){
	if(lazy[id]){
		t[id*2] += lazy[id];
		t[id*2+1] += lazy[id];
		lazy[id*2] += lazy[id];
		lazy[id*2+1] += lazy[id];
		lazy[id] = 0;
	}
}

void build(int l, int r, int id){
	if(l == r){
		t[id] = a[l];
		return;
	}

	int mid = (l + r)/2;

	build(l, mid, id*2);
	build(mid + 1, r, id*2+1);

	t[id] = max(t[id*2], t[id*2+1]);
}

long long get(int l, int r, int u, int v, int id){
	if(u > r || v < l) return INT_MIN;
	if(u <= l && r <= v) return t[id];

	int mid = (l + r)/2;

	doo(id);

	return max(get(l, mid, u, v, id*2), get(mid + 1, r, u, v, id*2+1));
}

void update(int l, int r, int u, int v, int gia_tri, int id){
	if(u > r || v < l) return;
	if(u <= l && r <= v){
		t[id] += gia_tri;
		lazy[id] += gia_tri;
		return;
	}

	int mid = (l + r)/2;
	doo(id);
	update(l, mid, u, v, gia_tri, id*2);
	update(mid + 1, r, u, v, gia_tri, id*2 + 1);
	t[id] = max(t[id*2], t[id*2+1]);
}

int main() {
  ios_base::sync_with_stdio(false);
	cin.tie(NULL); cout.tie(NULL);
	cin >> n >> q;
	memset(a, 0, sizeof(a));
	while(q--){
		int u, v;
        long gia_tri;
        cin >> u >> v >> gia_tri;
        update(1, n, u, v, gia_tri, 1);
	}
    long long res = get(1, n, 1, n, 1);
	cout << res;
	return 0;
}
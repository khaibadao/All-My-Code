using namespace std;

long long doo(string a){
	long long ans = 0;
	for(char x : a){
		ans += (x - '0');
	}
	return ans;
}

int main() {
    ios_base::sync_with_stdio(false);
	cin.tie(NULL); cout.tie(NULL);
	int t = 2;
	while(t--){
		long long n, k; cin >> n >> k;
		long sl = 1;
		long long res = k;
		vector <string> a;
		while(res <= n){
			res = k;
			res *= sl;
			++sl;
			a.pb(to_string(res));
		}
		a.pop_back();
		long long ans = 0;
		for(string s : a){
			// cout << s << en;
			ans += doo(s);
		}
		// cout << en;
		cout << ans << en;
	}
	return 0;
}

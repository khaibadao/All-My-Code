using namespace std;

bool check(vector <int> &a){
	a.pb(0);
	int n = Siz(a) - 1;
	for(int i = 0 ; i < n ; ++i){
		if(a[i]){
			if(a[i + 1] > 0 || (a[i - 1] > 0 && i > 0)) return true;
		}
	}
	return false;
}

int main() {
    ios_base::sync_with_stdio(false);
	cin.tie(NULL); cout.tie(NULL);
	int n; cin >> n;
	vector <int> a(n);
	int duong = 0, am = 0;
	for(int i = 0 ; i < n ; ++i){
		cin >> a[i];
		if(a[i] > 0) ++duong;
		if(a[i] < 0) ++am;
	}
	// cout << am << ' ' << duong;
	(check(a)) ? cout << min(duong, am) : cout << -1;
	return 0;
}

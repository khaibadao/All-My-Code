using namespace std;

int main()
{
	freopen("PCHAR.inp", "r", stdin);
	freopen("PCHAR.out", "w", stdout);
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	int n; cin >> n;
	string s; cin >> s;
	s = '%' + s;
	int ans = 0;
	int i = 1, j = 2;
	while(i < Siz(s) && j < Siz(s)){
		if(s[i] == '#'){
			if(s[j] == '.'){
				int res1 = 0;
				int j_k = j, i_k = i;
				while(s[j_k] == '.'){
			    	++res1;
					++j_k;
					if(s[j_k] != '.') break;
				}
				int res2 = 0;
				while(s[i_k] == '#'){
					if(i_k < 1) break;
					else
					{
						++res2;
						--i_k;
					}
				}
				ans += min(res1, res2);
				++i;
				j = i + 1;
				
//				nguoc = 0, xuoi = 0;
			}else{
				++i, j = i + 1;
			}
		}else{
			++i;
			j = i + 1;
		}
	}
	cout << ans;
	return 0;
}

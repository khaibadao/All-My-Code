using namespace std;

long ans = INT_MAX;
void doo(int x, vector <int> a, unordered_map <int, int> maa, int k){
	auto it = lower_bound(a.begin(), a.end(), x);
	int res = distance(a.begin(), it) - 1;
	int con_lai = k - maa[x];
	int i = res - 1, j = res + 1;
	long sum = 0;
	while(con_lai){
		int trai = x - a[i];
		int phai = a[j] - x;
		if(trai < phai){ // thằng bên trái có hiệu nhỏ hơn
			// thay tối đa 
			if(maa[a[i]] <= con_lai){
				sum += trai*maa[a[i]];
				con_lai -= maa[a[i]];
			}else
			{
				sum += trai*con_lai;
				con_lai = 0;
			}
		}else
		{
			if(maa[a[j]] <= con_lai){
				sum += phai*maa[a[j]];
				con_lai -= maa[a[j]];
			}else
			{
				sum += phai*con_lai;
				con_lai = 0;
			}
		}
		cout << con_lai << en;
	}
	ans = min(ans, sum);
	cout << sum << en;
}

using namespace std;

typedef unordered_map<long long, long long> unmap;

bool check(long n){
	if(n < 2) return false;
	if(n < 4) return true;
	if(n%2 == 0 || n%3 == 0) return false;
	for(long i = 5 ; i*i <= n ; i += 6){
		if(n%i == 0 || n%(i + 2) == 0) return false;
	}
	return true;
}

long long _gcd(long long a, long long b){
	if(b == 0) return a;
	return __gcd(b, a%b);
}

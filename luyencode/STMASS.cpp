using namespace std;

unordered_map <char, int> maa;


void doo(string s){
	maa['O'] = 16;
	maa['C'] = 12;
	maa['H'] = 1;
	stack <char> vi_tri;
	stack <long long> sum;
	long long res = 0;
	s = '(' + s;
	s += ')';
	s += '1';
	cout << s << en;
	// (((CH)2(OH2H)(CHO))3)1)
	for(int i = 0 ; i < Siz(s) - 1; ++i){
		if(s[i] == '('){
			if(res > 0){
				// có nghĩa là res có giá trị trước đó !
				sum.pop();
				sum.push(res);
			}
			sum.push(0);
			res = 0;
		}
		if(s[i] == 'H' || s[i] == 'C' || s[i] == 'O'){
			int sl = 1;
			if('0' <= s[i + 1] && s[i + 1] <= '9') sl = (s[i + 1] - '0');
			res += maa[s[i]]*sl;
		}
		if(s[i] == ')'){
			int sl = 1;
			if('0' <= s[i + 1] && s[i + 1] <= '9') sl = (s[i + 1] - '0');
			sum.pop();
			sum.push(res*sl);
			// res : là giá trị đại diện cho một cặp ngoặc
			long long ans1 = sum.top();
			sum.pop();
			long long ans2 = sum.top() + ans1;
			sum.pop();
			sum.push(ans2);
			cout << sum.top() << en;
		}
	}
}

int main() {
    ios_base::sync_with_stdio(false);
	cin.tie(NULL); cout.tie(NULL);
	maa['O'] = 16;
	maa['C'] = 12;
	maa['H'] = 1;
	int t; cin >> t;
	// cout << "Hello";
	while(t--){
		string s; cin >> s;
		s = '(' + s;
		// cout << s << en;
		doo(s);
	}
	return 0;
}

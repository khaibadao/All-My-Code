using namespace std;

int n, q;

long a[mang], t[mang*4];

void build(int l, int r, int id){
	if(l == r){
		t[id] = a[l];
		return;
	}
	int mid = (l + r)/2;
	build(l, mid, id*2);
	build(mid + 1, r, id*2+1);
	t[id] = min(t[id*2], t[id*2+1]);
}

long get(int l, int r, int u, int v, int id){
	if(l > v || u > r) return INT_MAX;
	if(u <= l && r <= v) return t[id];

	int mid = (l + r)/2;
	long get1 = get(l, mid, u, v, id*2);
	long get2 = get(mid + 1, r, u, v, id*2+1);

	return min(get1, get2);
}

int main() {
    ios_base::sync_with_stdio(false);
	cin.tie(NULL); cout.tie(NULL);
	
	cin >> n >> q;

	for(int i = 1 ; i <= n ; ++i) cin >> a[i];
	build(1, n, 1);
    while(q--){
    	int l, r; cin >> l >> r;
    	long ans = get(1, n, l, r, 1);
    	cout << ans << en;
    }
	return 0;
}
using namespace std;

string tmp = "";
double ans = 0.0;

void doo(string s){
  double res = 0, sl = 0;
  for(char x : s){
    if('0' <= x && x <= '9'){
      res += x - '0';
      ++sl;
    }
  }
  res = (double)(res/sl);
  if(res > ans){
    tmp = s;
    ans = res;
  }
}

int main() {

  freopen("TRONGSO.INP", "r", stdin);
  freopen("TRONGSO.OUT", "w", stdout);

  ios_base::sync_with_stdio(false);
  cin.tie(NULL); cout.tie(NULL);
  
  int t; cin >> t;
  while(t--){
    string s; cin >> s;
    doo(s);
  }

  (!tmp.empty()) ? cout << tmp : cout << 0;

  return 0;
}

using namespace std;

vector <int> a;
bool prime[1000001];
void sang()
{
  for(int i = 0 ; i <= 1000000 ; ++i) prime[i] = true;
  prime[0] = prime[1] = false;
  for(int i = 2 ; i <= 1000 ; ++i){
    if(prime[i]){
      for(int j = i * i ; j <= 1000000 ; j += i){
        prime[j] = false;
      }
    }
  }
  for(int i = 2 ; i <= 1000000 ; ++i)
  {
    if(prime[i]) a.pb(i);
  }
}

int main() {

  freopen("PAIRPBRO.INP", "r", stdin);
  freopen("PAIRPBRO.OUT", "w", stdout);

  ios_base::sync_with_stdio(false);
  cin.tie(NULL); cout.tie(NULL);

  sang();
  int n, k; cin >> n >> k;

  auto it = upper_bound(a.begin(), a.end(), n);
  int res = distance(a.begin(), it) - 1;
  int ans = 0;
  for(int i = 0 ; i <= res ; ++i){
    if(prime[a[i] + k] && a[i] + k <= n){
      ++ans;
    }
  }
  cout << ans;

  return 0;
}
